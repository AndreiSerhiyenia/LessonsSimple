package LessonsSimple.SoloLearn.ConditionalsAndLoops;

public class ForLoops {
    public static void main(String[] args) {
        for (int x = 1; x <= 3; x++) {
            System.out.println(x);
        }
        System.out.println();
        for (int x = 0; x <= 6; x = x + 2) {
            System.out.println(x);
        }
    }
}
